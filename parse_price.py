def parse_pdf(pdf_path):
    all_rows = []
    current_group = None

    with pdfplumber.open(pdf_path) as pdf:
        for page in pdf.pages:
            # 小薪邪褔邪谢邪 锌褉芯斜褍械屑 懈蟹胁谢械褔褜 褌邪斜谢懈褑褘
            tables = page.extract_tables()
            if tables:
                for table in tables:
                    for row in table:
                        if not row or not any(row):
                            continue
                        # 袨褔懈褖邪械屑 锌械褉胁褍褞 褟褔械泄泻褍
                        first_cell = str(row[0]).strip() if row[0] else ""
                        # 袩褉芯胁械褉褟械屑, 褟胁谢褟械褌褋褟 谢懈 褋褌褉芯泻邪 蟹邪谐芯谢芯胁泻芯屑 谐褉褍锌锌褘
                        is_group_header = False
                        for key, group_name in GROUP_MAP.items():
                            if key in first_cell:
                                current_group = group_name
                                is_group_header = True
                                break
                        if is_group_header:
                            continue  # 薪械 写芯斜邪胁谢褟械屑 蟹邪谐芯谢芯胁芯泻 泻邪泻 褌芯胁邪褉

                        # 袝褋谢懈 褋褌褉芯泻邪 褋芯写械褉卸懈褌 褌芯谢褜泻芯 薪邪蟹胁邪薪懈褟 谐褉褍锌锌, 薪芯 薪械 褋芯胁锌邪写邪械褌 褋 屑邪锌锌懈薪谐芯屑
                        if any(word in first_cell for word in ["孝褍屑斜谢械褉褘", "袣薪芯锌泻懈", "袩械褉械泻谢褞褔邪褌械谢懈", "袙褘泻谢褞褔邪褌械谢懈", "袦懈泻褉芯锌械褉械泻谢褞褔邪褌械谢懈", "袠蟹写械谢懈褟"]):
                            # 锌褉芯斜褍械屑 薪邪泄褌懈 胁 GROUP_MAP 锌芯 褔邪褋褌懈褔薪芯屑褍 褋芯胁锌邪写械薪懈褞
                            for key, group_name in GROUP_MAP.items():
                                if key.split()[0] in first_cell or first_cell.split()[0] in key:
                                    current_group = group_name
                                    break
                            else:
                                current_group = first_cell  # 械褋谢懈 薪械 薪邪褕谢懈, 褋芯褏褉邪薪褟械屑 泻邪泻 械褋褌褜
                            continue

                        # 袩褉芯胁械褉褟械屑 薪邪谢懈褔懈械 褌褉褢褏 泻芯谢芯薪芯泻 (懈蟹写械谢懈械, 褑械薪邪1, 褑械薪邪2)
                        if len(row) >= 3:
                            product = str(row[0]).strip()
                            price_bulk_str = str(row[1]).strip()
                            price_retail_str = str(row[2]).strip()
                            # 袩褉芯锌褍褋泻邪械屑 褋褌褉芯泻懈-蟹邪谐芯谢芯胁泻懈 褌邪斜谢懈褑
                            if product in ["袧邪懈屑械薪芯胁邪薪懈械 懈蟹写械谢懈褟", "袨褌锌褍褋泻薪邪褟 褑械薪邪", "锌邪褉褌懈褟 斜芯谢械械 100褕褌", "锌邪褉褌懈褟 屑械薪械械 100褕褌"]:
                                continue
                            if not product or not price_bulk_str or not price_retail_str:
                                continue
                            # 袩褉械芯斜褉邪蟹褍械屑 褑械薪褘
                            try:
                                bulk = float(price_bulk_str.replace(" ", "").replace(",", ".").replace("聽", ""))
                            except:
                                bulk = None
                            try:
                                retail = float(price_retail_str.replace(" ", "").replace(",", ".").replace("聽", ""))
                            except:
                                retail = None
                            if bulk is not None and retail is not None:
                                all_rows.append({
                                    "product": product,
                                    "group": current_group if current_group else "袧械懈蟹胁械褋褌薪邪褟 谐褉褍锌锌邪",
                                    "price_bulk": bulk,
                                    "price_retail": retail
                                })
            else:
                # 袝褋谢懈 褌邪斜谢懈褑褘 薪械 薪邪泄写械薪褘, 锌褉芯斜褍械屑 懈蟹胁谢械褔褜 褌械泻褋褌 懈 懈褋泻邪褌褜 锌邪褉褘 "懈蟹写械谢懈械 - 褑械薪邪"
                # (褍锌褉芯褖褢薪薪褘泄 胁邪褉懈邪薪褌, 薪芯 胁 胁邪褕械屑 PDF 褌邪斜谢懈褑褘 械褋褌褜, 锌芯褝褌芯屑褍 褝褌芯褌 斜谢芯泻 褋泻芯褉械械 薪械 锌芯薪邪写芯斜懈褌褋褟)
                pass

    return all_rows

def save_to_db(rows):
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    cursor.execute("DELETE FROM prices")
    for row in rows:
        cursor.execute("""
            INSERT INTO prices (product_name, unit, price_bulk, price_retail, group_name)
            VALUES (?, ?, ?, ?, ?)
        """, (row["product"], "褕褌.", row["price_bulk"], row["price_retail"], row["group"]))
    conn.commit()
    conn.close()

